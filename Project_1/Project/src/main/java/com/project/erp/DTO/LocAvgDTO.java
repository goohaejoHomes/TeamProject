package com.project.erp.DTO;


public class LocAvgDTO {
	
	private int loc_no;
	private String loc_name;
	private int month_rent;
	private int rent_deposit;
	
	public int getLoc_no() {
		return loc_no;
	}
	public void setLoc_no(int loc_no) {
		this.loc_no = loc_no;
	}
	public String getLoc_name() {
		return loc_name;
	}
	public void setLoc_name(String loc_name) {
		this.loc_name = loc_name;
	}
	public int getMonth_rent() {
		return month_rent;
	}
	public void setMonth_rent(int month_rent) {
		this.month_rent = month_rent;
	}
	public int getRent_deposit() {
		return rent_deposit;
	}
	public void setRent_deposit(int rent_deposit) {
		this.rent_deposit = rent_deposit;
	}

	
	
}